        function new_location(pincode) {

            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });

            // event.preventDefault();
            var pincode = pincode;
            $.ajax({
                type: 'POST'
                , url: "{{ route('location_select')}}"
                , data: {
                    pincode: pincode

                }
                , success: function(data) {
                    var val = JSON.parse(data);
                    $('.new_area').html(val.area);
                    $('.new_city').val(val.city);
                    $('.new_district').val(val.district);
                    $('.new_state').val(val.state);
                    $('.new_country').val(val.country);
                    $('.new_city_code').val(val.city_code);


                }
            });


        }

    </script>


    <script>
        /* Function to generate combination of password */
        function generateP() {
            var pass = '';
            var str = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ' +
                'abcdefghijklmnopqrstuvwxyz0123456789@#$';

            for (let i = 1; i <= 8; i++) {
                var char = Math.floor(Math.random() *
                    str.length + 1);

                pass += str.charAt(char)
            }

            return pass;
        }

        function gfg_Run() {
            document.getElementById("token_refresh").value = generateP();
        }

    </script>
